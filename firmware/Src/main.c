uint32_t HX_Get_Value(uint8_t gain);
void HX_Delay(uint32_t u);

struct HX711_t{
  uint32_t rawData;
  int32_t weight_ug;
  uint8_t gain;
  uint8_t rate;
  uint8_t clock;
} HX711;

int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C2_Init();
  MX_SPI1_Init();
  MX_TIM1_Init();
  MX_TIM16_Init();
  MX_TSC_Init();
  MX_USB_PCD_Init();

  HAL_GPIO_WritePin(GPIOB,HX_CLK_Pin,0);

  while (1)
  {
    if(!HAL_GPIO_ReadPin(GPIOB,HX_INT_Pin)){
      HX_Get_Value(HX_GAIN_128);
      HAL_GPIO_TogglePin(GPIOA, LED_Pin);
    } else {
      HAL_Delay(1);
      HAL_GPIO_WritePin(GPIOB,HX_SPS_Pin,HX711.rate);
    }
  }
}


uint32_t HX_Get_Value(uint8_t gain) {
  for(int i = 0; i < gain; i++){
    HAL_GPIO_WritePin(GPIOB,HX_CLK_Pin,1);
    HX_Delay(1);
    HAL_GPIO_WritePin(GPIOB,HX_CLK_Pin,0);
    if(i < 24){
      HX711.rawData |= (HAL_GPIO_ReadPin(GPIOB,HX_DATA_Pin) << (24 - i));
    }
    HX_Delay(1);
  }
}

void HX_Delay(uint32_t u) {
    u = u * MULTIPLIER - 10;
    while (u--);
}

